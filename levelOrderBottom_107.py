        queue.append(root)

        while queue:
            cur = []
            for _ in range(len(queue)):
                node = queue.popleft()
                cur.append(node.val)
                if node.left:
                    queue.append(node.left)
                if node.right:
                    queue.append(node.right)

            tmp.append(cur)
        
        for _ in range(len(tmp)):
            result.append(tmp.pop())
        
        return result
